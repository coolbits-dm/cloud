CACHE_FILE = os.path.join(CACHE_DIR, 'responses.json')

def load_cache():
    if os.path.exists(CACHE_FILE):
        with open(CACHE_FILE, 'r') as f:
            return json.load(f)
    return {}

def save_cache(cache):
    with open(CACHE_FILE, 'w') as f:
        json.dump(cache, f)

def tone_print(tone, message):
    click.echo(message)

def ensure_backup(path):
    if os.path.exists(path):
        backup = f"{path}.bak"
        if not os.path.exists(backup) or os.path.getmtime(path) > os.path.getmtime(backup):
            with open(path, 'r') as src, open(backup, 'w') as dest:
                dest.write(src.read())

def backup_and_write(path, data):
    ensure_backup(path)
    with open(path, 'w') as f:
        f.write(data)

def backup_and_append(path, data):
    ensure_backup(path)
    with open(path, 'a') as f:
        f.write(data)

def safe_reload_nginx(logger):
    if os.system("nginx -t") != 0:
        logger("nginx -t failed; reload aborted.")
        return False
    if os.system("systemctl reload nginx") != 0:
        logger("systemctl reload nginx failed.")
        return False
    return True

